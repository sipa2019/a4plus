                <p>Это, сравнительно молодая, и мало распространённая технология, которая, в отличие от известных &laquo;горячих&raquo; технологий, проводится при цеховой температуре, без принудительного нагрева, растворения обрабатываемого материала. </p>

<p>Работы в направлении изучения и промышленного внедрения ТХД получили распространение в конце прошлого века, благодаря трудам учёных-химиков Европы, Японии, Индии, России, Литвы, Малайзии и др. стран. Появились серьёзные предпосылки, для её широкого применения по всему миру. Разрабатываются разные типы хим. реагентов для проведения процесса. </p>

<p><strong>Однако, процесс распространения ТХД по миру идёт медленно, в силу ряда причин, среди которых главными являются: </strong></p>

			<ul>
				<li>Отсутствие реальных промышленных внедрений в виде действующих референц-предприятий; </li>
				<li>&laquo;Технологическая&raquo; консервативность производственников-резинщиков, не спешащих экспериментировать с включением девулканизата в &laquo;привычные&raquo;, отработанные рецептуры новых резиновых смесей; </li>
				<li>Слабые возможности разработчиков в продвижении своих &laquo;Know-how&raquo; на рынок; </li>
				<li>Конкуренция на рынке технологий рециклинга эластомеров со стороны &laquo;традиционников&raquo;. </li>
			</ul>

<p>Производство рез. крошки из утильных шин на сегодняшний день является самой распространённой в мире технологией их переработки, но ареал применения ограничен из-за слабой совместимости её вулканизированной составляющей с компонентами сырых резиновых смесей. Не спасает положение и попытки измельчения крошки до уровня дисперсных фракций. Поверхность мелкодисперсных частиц не становится более совместимой с компонентами сырой резины, а производство мелкодисперсной крошки слишком затратно. </p>

<p>Здесь нельзя не заметить, что, для производителей крошки автошин, имеющих трудности с реализацией своей продукции, в силу достаточно узкого ареала её применения и наблюдающимся, практически везде, &laquo;перегреве&raquo; рынка, ТХД может стать отличным дополнением к их производствам, решающим эти проблемы, значительно повышающим цену конечного продукта.</p>


