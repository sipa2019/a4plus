		                    <div class="alert alert-success">
		                    
		                    Девулканизация – это технология переработки отходов эластомеров, позволяющая вернуть материалу способность к повторной вулканизации.
							
							</div>
					
									<p>Существуют в применении разного уровня методы девулканизации резин: термо-механический экструзивный, метод ожижения резины (щелочной, водо-нейтральный) с последующей сушкой, паровой метод.</p>

									<p>Все они проводятся с принудительным нагревом обрабатываемого материала, требуют высоких затрат эл. и тепловой энергии, времени, дорогостоящего сложного оборудования, малопродуктивны, и в конечном итоге, качество регенерата на выходе не позволяет возвращать его большим количеством в баланс резиновой отрасли.</p>

									<p>«Традиционный» метод девулканизации, который практикуется по всему миру давно и широко, особенно в Азии, - это метод «автоклавной регенерации». Старейшим и самым крупным производителем регенерата резины в России является Чеховский регенератный завод (Московская обл.)</p>

									<p>Регенераты, получаемые «автоклавным методом», т.е. воздействием на материал хим. реагентами, под давлением и при температурах >250 ℃, несмотря на свои, достаточно высокие физические характеристики (адгезивность, пластичность и др.), химическими похвастать не могут. Следствием «жёстких» условий проведения автоклавной технологии является разрушение, деструкция макромолекул каучуков, влекущие к резкому ухудшению качества регенерата, и прежде всего, его прочностных показателей, которые количественно на порядок ниже, нежели у качественных резин.</p>

									<p>Такая, мало продвинутая ситуация в области девулканизации отходов эластомеров диктует необходимость появления новых рентабельных технологий. Сегодня к ним смело можно отнести «Технологию «холодной» девулканизации (ТХД).</p>

									<p>В процессе ТХД крошка отработанных шин, при многократном пропускании обрабатываемого материала через зазор между валками вальцев (рис. вверху стр.), подвергается интенсивной деформации перетиранию и смешиванию, с добавленным заранее в материал хим. реагентом –девулканизатором, или, как его называют в некоторых источниках, - модификатором.</p>

									<p>Под суммарным воздействием физико-химического процесса девулканизации разрушаются серные цепочки, соединяющие полимерные молекулы эластомера, что в свою очередь, ведёт, к восстановлению в поверхностном слое частиц резиновой крошки способности к повторной вулканизации. При этом, собственно молекулы полимера, в подавляющем большинстве, не подвергаются разрушению.</p>

									<p>Главным, недостатком данной технологии является разнородность химического материала резиновых смесей, из которых изготавливаются шины разными компаниями-производителями. Состав таких смесей у каждого производителя является предметом know-how. И, хотя по составу основных компонентов (каучуки, тех. углерод) они различаются незначительно, включение девулканизированной резины, изготовленной из разнообразных резиновых отходов, в новых смесях может влиять на некоторые конечные качественные характеристики новых смесей.</p>

									<p>Этот недостаток вполне компенсируется выбором оптимального количества добавляемой девулканизированной резины в новые смеси, которое может колебаться от 10 - 20% в новых шинах, до 80% в других изделиях без снижения качества конечной смеси.</p>
                   
